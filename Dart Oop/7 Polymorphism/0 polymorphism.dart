/*
Two types of Java, C++ polymorphism
1. Compile-time Polymorphism
2. Runtime Polymorphism

But Dart does not support Compile-time Polymorphism (function or method overloading).

  ===>> Compile-Time Polymorphism <<===

It is also known as static polymorphism. This type of polymorphism is 
achieved by "function overloading" or "operator overloading".



  ===>> Run-Time Polymorphism <<===

It is also known as Dynamic Method Dispatch. It is a process in which 
a function call to the overridden method is resolved at Runtime. This
type of polymorphism is achieved by Method Overriding. Method overriding, 
on the other hand, occurs when a derived class has a definition for one 
of the member functions of the base class. That base function is said to 
be overridden.

*/